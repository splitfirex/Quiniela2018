package quiniela.model;

public class TeamGroupDetails {

    private int p = 0;
    private long pg = 0;
    private long ng = 0;

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public long getPg() {
        return pg;
    }

    public void setPg(long pg) {
        this.pg = pg;
    }

    public long getNg() {
        return ng;
    }

    public void setNg(long ng) {
        this.ng = ng;
    }
}
